def add_numbers(a: int, b: int) -> int:
    """Add two numbers together.

    Args:a
      a (int): a number
      b (int): a number

    Returns:
      (int). The sum of the two numbers.
    """
    return a + b


def subtract_numbers(x: int, y: int) -> int:
    """Subtract two numbers together.

    Args:a
      x (int): a number
      y (int): a number

    Returns:
      (int). The difference of the two numbers.
    """
    return x - y
